
/**
 * @file fibo.h
 * @brief Fibonacci number calculation.
 *
 * Implements the Fibonacci sequence using an iterative (non-recursive) approach.
 */

/**
 * @brief Calculates the nth Fibonacci number iteratively.
 *
 * Uses an iterative approach with O(n) time complexity and O(1) space.
 *
 * @param num The position in the Fibonacci sequence (1-based).
 * @return The nth Fibonacci number.
 */
int fibo(int num);
